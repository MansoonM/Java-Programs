public class operators {
    public static void main(String[] args) {
        System.out.println("Operators: There are 8 Operators in java.");

        // 1. Arithmetic Operators ( +, -, *, /, %)
        int num1 = 40;
        int num2 = 20;
        System.out.println("Addition of num1(40) and num2(20): "+ (num1 + num2));
        System.out.println("Substraction of num1(40) and num2(20): "+ (num1 - num2));
        System.out.println("Multiplication of num1(40) and num2(20): "+ (num1 * num2));
        System.out.println("Division of num1(40) and num2(20): "+ (num1 / num2));
        System.out.println("Remainder of num1(40) and num2(20): "+ (num1 % num2));

    }
}
